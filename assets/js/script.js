document.addEventListener("DOMContentLoaded", function () {
    const menuToggle = document.querySelector(".menu-toggle"); // Bot√≥n hamburguesa
    const menu = document.querySelector("nav ul.menu"); // Men√∫ desplegable
    const menuLinks = document.querySelectorAll("nav ul.menu li a"); // Enlaces del men√∫
    const header = document.querySelector("header"); // Encabezado fijo

    console.log("üîé Verificando elementos del men√∫ hamburguesa...");
    console.log("üìå Bot√≥n men√∫ hamburguesa:", menuToggle);
    console.log("üìå Men√∫:", menu);
    console.log("üìå Enlaces del men√∫:", menuLinks.length);

    if (!menuToggle || !menu || menuLinks.length === 0) {
        console.error("‚ö†Ô∏è Error: No se encontraron los elementos del men√∫ en el DOM.");
        return;
    }

    // üìå Alternar el men√∫ al hacer clic en el bot√≥n hamburguesa
    menuToggle.addEventListener("click", function (e) {
        e.stopPropagation();
        menu.classList.toggle("active");
        console.log("üîπ Men√∫ hamburguesa activado/desactivado");
    });

    // üìå Cerrar el men√∫ despu√©s de hacer clic en una opci√≥n y navegar correctamente
    menuLinks.forEach(link => {
        link.addEventListener("click", function (e) {
            e.preventDefault();

            const targetId = this.getAttribute("href").substring(1);
            const targetElement = document.getElementById(targetId);

            if (targetElement) {
                console.log(`üîπ Navegando a: #${targetId}`);

                // üìå Alternativa para m√≥viles: scrollIntoView en lugar de scrollTo
                targetElement.scrollIntoView({
                    behavior: "smooth",
                    block: "start"
                });

                // üìå Cerrar el men√∫ despu√©s de la animaci√≥n
                setTimeout(() => {
                    menu.classList.remove("active");
                    console.log("üîπ Men√∫ hamburguesa cerrado despu√©s de la animaci√≥n");
                }, 500);

                // üìå Actualizar la URL
                history.pushState(null, null, `#${targetId}`);
            } else {
                console.error(`‚ö†Ô∏è Elemento con ID '${targetId}' no encontrado.`);
            }
        });
    });

    // üìå Cerrar el men√∫ si se toca fuera de √©l
    document.addEventListener("click", function (event) {
        if (!menu.contains(event.target) && !menuToggle.contains(event.target)) {
            menu.classList.remove("active");
            console.log("üîπ Men√∫ hamburguesa cerrado al hacer clic fuera");
        }
    });
});

